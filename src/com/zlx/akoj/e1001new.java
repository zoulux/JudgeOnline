//不是清泉，是天上虹；
//揉碎在浮藻间，
//沉淀着彩虹似的梦。
//----
//寻梦？撑一支长篙⑶，
//向青草更青处漫溯⑷；
//满载一船星辉，
//在星辉斑斓里放歌。
//----
//但我不能放歌，
//悄悄是别离的笙箫；
//夏虫也为我沉默，
//沉默是今晚的康桥！
//----
//悄悄的我走了，
//正如我悄悄的来；
//我挥一挥衣袖，
//不带走一片云彩。//再别康桥
//轻轻的我走了，
//正如我轻轻的来；
//我轻轻的招手，
//作别西天的云彩。
//----
//那河畔的金柳，
//是夕阳中的新娘；
//波光里的艳影，
//在我的心头荡漾。
//----
